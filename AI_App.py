async def Image_pipeline(scenes: list) -> None:
    """
    Full pipeline:
      - Generate image(s)
      - For every output image, launch a video gen job concurrently
    """
    from MAIN_AI_PIPELINE import run_Flux_Image_Gen


    with ThreadPoolExecutor(max_workers=MAX_WORKERS) as executor:


        # Launch image generation for every prompt
        image_tasks = [
            asyncio.create_task(run_Flux_Image_Gen(scene, executor))
            for scene in scenes   # EACH scene is a dict
        ]

        image_results = await asyncio.gather(*image_tasks)

        # Flatten list (because each prompt returns a list of image paths)
        image_paths = []
        for result in image_results:
            image_paths.extend(result)

        print(f"[MAIN] ✔ Generated {len(image_paths)} image(s)")

async def Video_pipeline(video_jobs: list) -> None:
    """
    Full pipeline:
      - Generate video(s)
      - For every output image, launch a video gen job concurrently
    """
    from MAIN_AI_PIPELINE import run_WAN_I2V_Gen

    with ThreadPoolExecutor(max_workers=MAX_WORKERS) as executor:

        # Launch Video generation for every image path
        video_tasks = [
            asyncio.create_task(run_WAN_I2V_Gen(job["path"], job["positive_prompt"], job["negative_prompt"], executor))
            for job in video_jobs
        ]

        Video_results = await asyncio.gather(*video_tasks)

        # Flatten list
        generated_videos = []
        for result in Video_results:
            if isinstance(result, list):
                generated_videos.extend(result)
            elif result:
                generated_videos.append(result)

        print(f"[MAIN] ✔ Generated {len(generated_videos)} Video(s)")

