def process_scenes(json_input):

    # Convert string → JSON if necessary
    if isinstance(json_input, str):
        data = json.loads(json_input)
    else:
        data = json_input

    scene_visuals = []
    scene_animations = []
    scene_text = []
    vo_lines = []
    scene_vo_lines = []
    scene_durations = []
    historical_photos = []

    for item in data:

        scene = item.get("scene")

        primary_visual = item.get("primary_visual_image")
        animation = item.get("animation")
        negative_animation = item.get("negative_video_prompt")
        historical_photo = item.get("historical_photo", False)
        ref_visual = item.get("reference_visual")
        print("NIGAAAAAAAAAAA:  ", scene, primary_visual, historical_photo, ref_visual)


        if historical_photo is True or ref_visual is not None:
            primary_visual = None
        

        if primary_visual is None and historical_photo is False and ref_visual is not None:
            import urllib.request
            import os
            
            ext = os.path.splitext(ref_visual.split('?')[0])[-1]
            if not ext or ext == ".png":
                ext = ".jpg"
            
            
                
            filename = f"scene{scene}_reference{ext}"
            save_path = os.path.join(IMAGE_DIR, filename)
            
            try:
                req = urllib.request.Request(ref_visual, headers={'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36'})
                with urllib.request.urlopen(req) as response, open(save_path, 'wb') as out_file:
                    out_file.write(response.read())
                print(f"[INFO] Downloaded reference visual for scene {scene} to {save_path}")
            except Exception as e:
                print(f"[ERROR] Failed to download reference visual for scene {scene}: {e}")
                
            # Skip adding to scene_visuals so it's not generated by Flux
            # We can still add it with a null prompt if we want it preserved in output
            scene_visuals.append({
                "scene": scene,
                "primary_visual_image": None
            })

        elif primary_visual is None and ref_visual is None and historical_photo is True:
            def blank_function():
                pass
            blank_function()
            scene_visuals.append({
                "scene": scene,
                "primary_visual_image": None
            })
            
        else:
            # Scene + primary visual
            scene_visuals.append({
                "scene": scene,
                "primary_visual_image": primary_visual
            })

        if primary_visual is None:
            animation = None
            negative_animation = None


        # Scene + animation
        scene_animations.append({
            "scene": scene,
            "animation": animation,
            "negative_video_prompt": negative_animation
        })

        # Scene + text
        scene_text.append({
            "scene": scene,
            "small": item.get("on_screen_text_small"),
            "bold": item.get("on_screen_text_bold"),
            "large": item.get("on_screen_text_large")
        })

        # Collect VO lines
        if item.get("vo_line"):
            vo_lines.append(item["vo_line"])
            
        scene_vo_lines.append({
            "scene": scene,
            "vo_line": item.get("vo_line")
        })

        scene_durations.append({
            "scene": scene,
            "duration_seconds": item.get("duration_seconds")
        })

        # Historical photos
        if item.get("historical_photo") is True:
            historical_photos.append({
                "scene": scene,
                "historical_photo_description": item.get("historical_photo_description"),
                "vo_line": item.get("vo_line", ""),
                "duration_seconds": item.get("duration_seconds", "")
            })

    # Combine VO lines into one string
    combined_vo = " ".join(vo_lines)

    return {
        "scene_visuals": scene_visuals,
        "scene_animations": scene_animations,
        "scene_text": scene_text,
        "vo_script": combined_vo,
        "scene_vo_lines": scene_vo_lines,
        "scene_durations": scene_durations,
        "historical_photos": historical_photos
    }

