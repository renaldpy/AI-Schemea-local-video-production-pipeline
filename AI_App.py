    def load_image_seq_data(self):
        # Read from Historical Photos dir
        self.historical_images = []
        if os.path.exists(self.dir_historical_photos):
            for file in os.listdir(self.dir_historical_photos):
                if file.lower().endswith(('.png', '.jpg', '.jpeg', '.gif')):
                    self.historical_images.append(os.path.join(self.dir_historical_photos, file))
                    
        # Read from JSON
        self.seq_scenes = []
        if os.path.exists(JSON_FILE):
            try:
                with open(JSON_FILE, 'r', encoding='utf-8') as f:
                    data = json.load(f)
                
                hist_photos = data.get("historical_photos", [])
                for hp in hist_photos:
                    if hp:
                        self.seq_scenes.append(hp)
            except Exception as e:
                print(f"[ERROR] Loading sequence data: {e}")
                
        if self.seq_scenes:
            self.seq_current_index = 0
            self.display_seq_scene()
        else:
            self.seq_lbl_desc.config(text="No historical photo scenes found.")
            for widget in self.seq_image_frame.winfo_children():
                widget.destroy()

