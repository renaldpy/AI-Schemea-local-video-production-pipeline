    def set_aspect_ratio(self, ratio: str):
        """Store the selected aspect ratio for the current scene and update button styles."""
        if not self.seq_scenes:
            return

        scene_id = self.seq_scenes[self.seq_current_index].get("scene", "?")
        self.seq_aspect_ratios[scene_id] = ratio

        # Update button appearance to reflect active selection
        self._refresh_aspect_buttons(ratio)

    def _refresh_aspect_buttons(self, active_ratio: str):
        """Visually highlight the active aspect ratio button."""
        if active_ratio == "4:3":
            self.btn_aspect_43.configure(style="AspectActive.TButton")
            self.btn_aspect_916.configure(style="AspectInactive.TButton")
        else:
            self.btn_aspect_43.configure(style="AspectInactive.TButton")
            self.btn_aspect_916.configure(style="AspectActive.TButton")

    # ── Generate Image Scenes trigger ─────────────────────────────────────────

    def trigger_image_sequence_generation(self):
        """Collect duration_seconds for every scene and invoke Image_sequence_generation."""
        if not self.seq_scenes:
            messagebox.showwarning("No Scenes", "No historical photo scenes are loaded.")
            return

        # Build list of (scene_id, duration_seconds) from all loaded scenes
        scene_durations = [
            {
                "scene": s.get("scene"),
                "duration_seconds": s.get("duration_seconds"),
                "aspect_ratio": self.seq_aspect_ratios.get(s.get("scene"), None)
            }
            for s in self.seq_scenes
        ]

        print(f"[INFO] Triggering Image_sequence_generation for {len(scene_durations)} scene(s).")

        threading.Thread(
            target=lambda: Image_sequence_generation(scene_durations),
            daemon=True
        ).start()

